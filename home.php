<ul class = 'paragrafo'>
	<li>Principais cuidados:
		<ul>
			<li>Água, vasos ou recipientes que receberem as flores devem estar limpos.</li>
			<li>Troque a água todos os dias.</li>
			<li>Para que as flores não destratem mais rápido, não coloque-as expostas diretamente a luz do sol.</li>
			<li>Retire todas as folhas que por ventura ficarem submersas na água, impedindo assim a proliferação de bactérias.</li>
			<li>Mantenha-as em local fresco e ventilado.</li>
			<li>Corte diariamente o extremo do caule.</li>
		</ul>
	</li><br>
	<li>Rosas Vermelhas:
		<ul>
			<li>Corte o caule em diagonal a 2cm da base das rosas.</li>
			<li>Coloque o buquê de flores em um vaso com água limpa.</li>
			<li>Retire as folhas que ficarem submersas.</li>
			<li>Mantenha as flores em local arejado.</li>
			<li>Coloque ½ colher de café de água sanitária para cada litro de água para evitar a formação de fungos.</li>
			<li>Plante-as num colocal em que há exposição de sol por, no mínimo, 6 horas por dia.</li>
			<li>Enriqueça a terra com composto orgânico.</li>
			<li>Regue as rosas uma vez por semana.</li>
			<li>A temperatura ideal para florescimento é entre 23°C e 25°C durante o dia e a 15°C e 18°C a noite.</li>
		</ul>
	</li><br>
	<li>Orquídeas:
		<ul>
			<li>Dê preferência a uma espécie que se adapte a sua região.</li>
			<li>Não plante na terra, pois muitas não sobrevivem à umidade excessiva.</li>
			<li>Mantenha o vaso úmido, mas sem exageros e lembre-se de deixar o excesso de água escorrer.</li>
			<li>Regue 2 ou 3 vezes na semana.</li>
			<li>Deixe no sol até às 9 da manhã ou após as 16 horas.</li>
			<li>Cultive as flores em local arejado.</li>
			<li>Essas flores são uma ótima opção de presente, pois simbolizam força, luxo, beleza e amor.</li>
		</ul>
	</li>
</ul>
